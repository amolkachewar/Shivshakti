	        <h2 style="text-align:center;color:blue;"><u><b>DDUGKY</b> </u></h2>
	       <p class="para"> DDUGKY PROJECT – DIN DAYAL UPADHYAY GRAMIN KAUSHAL YOJANA PROJECT Deen Dayal Upadhyaya Grameen Kaushalya Yojana (DDUGKY), the skill training and placement program of the Ministry of Rural Development (MoRD), occupies a unique position amongst other skill training programmes, due to its focus on the rural poor youth and its emphasis on sustainable employment through the prominence and incentives given to post-placement tracking, retention and career progression. Even as India moves towards becoming an international skills hub, there is a need to acknowledge the challenges preventing the rural poor from taking advantage of this momentous economic transition. Lack of formal education, marketable skills and other constraints imposed by poverty place significant entry barriers to today’s job market. DDU-GKY is therefore designed to not only provide high quality skill training opportunities to the rural poor, but also to establish a larger ecosystem that supports trained candidates secure a better future. The design of DDU-GKY, as detailed below, represents the strategy of the programme aimed at making rural India a key partner to India’s growth story.

DDUGKY follows a 3-tier implementation model. The DDU-GKY National Unit at MoRD is the agency responsible for national policy-making, funding, technical support and facilitation. The DDU-GKY State Skill Missions embedded in general within the State Rural Livelihood Missions (SRLMs), are envisioned to play a central role in providing co-funding and implementation support to DDU-GKY in the state; and the Project Implementing Agencies (PIAs) who implement the programme through skill training and placement projects.</p>
