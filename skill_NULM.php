<b>NULM Mission</b>

<p class="para">To reduce poverty and vulnerability of the urban poor households by enabling them to access
gainful self-employment and skilled wage employment opportunities, resulting in an appreciable
improvement in their livelihoods on a sustainable basis, through building strong grassroots level
institutions of the poor. The mission would aim at providing shelter equipped with essential services
to the urban homeless in a phased manner. In addition, the Mission would also address livelihood
concerns of the urban street vendors by facilitating access to suitable spaces, institutional credit,
social security and skills to the urban street vendors for accessing emerging market opportunities.</p>

<b>Guiding Principles</b>
<p class="para">The core belief of National Urban Livelihoods Mission (NULM) is that the poor are
entrepreneurial and have innate desire to come out of poverty. The challenge is to unleash their
capabilities to generate meaningful and sustainable livelihoods. The first step in this process is
motivating the urban poor to form their own institutions. They and their institutions need to be
provided sufficient capacity so that they can manage the external environment, access finance,
expand their skills, enterprises and assets. This requires continuous and carefully designed
handholding support. An external, dedicated and sensitive support structure, from the national
level to the city and community levels, is required to induce social mobilisation, institution building
and livelihood promotion.</p>

<p class="para">NULM would place a very high emphasis on convergence with schemes/programmes of the
relevant line Ministries/Departments and programmes of state governments dealing with skills,
livelihoods, entrepreneurship development, health, education, social assistance, etc. An alliance
strategy will be sought with all concerned departments to promote skills training of rural-urban
migrants as a bridge between the livelihoods of the rural and urban poor.</p>

<b>Values</b>
<h5>The mission will espouse the following values:</h5>


<p class="para">i Ownership and productive involvement of the urban poor and their institutions in
 all processes;</p>
<p class="para">ii Transparency in programme design and implementation, including institution
 - building and capacity strengthening;</p>
<p class="para">iii Accountability of government functionaries and the community;</p>
<p class="para">iv Partnerships with industry and other stakeholders; and</p>
<p class="para">v Community self-reliance, self-dependence, self-help and mutual-help.</p>

<b>Guidelines for Implementation</b>
<p class="para">The Mission Directorate in the Ministry of Housing and Urban Poverty Alleviation,
Government of India will issue a set of detailed operational guidelines from time to time for each
component and sub-component of NULM, for effective operationalisation, implementation and
monitoring of the Mission. </p>
