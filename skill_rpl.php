	        <h2> RPL</h2>
	       <p class="para"> <b>Recognition of Prior Learning (RPL)</b> is a platform to provide recognition to the informal learning or learning through work to get equal acceptance as the formal levels of education. It aims to appreciate prior learning irrespective of the medium of achieving it. In short, RPL is a process of assessment of an individual’s prior learning to give due importance to learning as an outcome rather than learning as process.

To ensure that the candidates being assessed under RPL are also oriented to the standardized NSQF levels, QP-NOSs that would be followed under RPL will be same as the one followed under fresh training. Further, to ensure the acceptance of RPL in Indian market, various pilots have been conducted by different sector skill councils and NSDA and the learnings are incorporated while preparing the guidelines for RPL in the PMKVY.

Under PMKVY, special focus is given to RPL by recognizing prior competencies of the assessed candidates and provides a certificate and monetary reward on successful completion of assessments. Average monetary reward under RPL would be around Rs.2,200 per candidate. Following allocation has been done under RPL in the Pradhan Mantri Kaushal Vikas Yojana (PMKVY).

It is mandated  that  following  reward  amount  be  left  for  the  trainee.  This  will encourage the trainee to participate in the process of RPL. </p>

<br>

<h3>Guidelines and Implementation Strategy</h3>

<b>Under RPL, the candidate can be certified in two ways:</b>

<p>Full Qualification

In this scenario, a candidate enrolled under RPL is assessed as per the assessment criteria of the QP-NOS of the selected job role. If the candidate clears competency based assessment under the designated Job Role, candidate is awarded a “Full Qualification” Certificate.</p>

<h3>Partial Qualification </h3>

<p>NOS Based Certification Under this scenario, a candidate enrolled under RPL is assessed as per the same QP-NOS. The SSCs have to define the minimum assessment criteria that the trainee has to attain for the QP-NOS in order to get NOS based certificate.</p>

<h3>For morre infomation read the Process Manual and RPL Guide</h3>

<p>Process to apply for Pre-screening for RPL under PMKVY

Step 1: Allocation of RPL number by IT ITeS SSC NASSCOM to the TP Concern

Step 2: TP mobilizes the candidates for RPL with reference to the QP Concern

Step 3: TP needs to pre-screen the candidate using either one of the options provided below: 

Option 1: TP conducts the pre-screening and assesses the competency/readiness of the candidate for RPL QP Assessment
Option 2: TP opts for online pre-screening assessment at cost provided by IT-ITeS Sector Skills Council NASSCOM (SSC NASSCOM) 
Register for pre-screening Assessment at the rate of INR 160 inclusive of Taxes per candidate per attempt
Candidate can re-attempt the pre-screening assessment by re-registering and paying INR 160 once again
Step 4:  Post the pre-screening assessment, candidates will receive their scorecard on clicking on the submit button; TP can decied to register the candidate for RPL QP assessments baised on the candidates pre-screening performance</p>

