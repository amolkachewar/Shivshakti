	        <h2 style="text-align:center;color:blue;"> <u><b>MSSDS</b></u></h2>
	       <p class="para">Under the leadership of Hon. Prime Minister of India, Skill Development has been spelt out as the need of the hour and for this purpose the target of creating 500 million skilled people by 2022 has been set.</p>

<p class="para">In line with the Hon. Prime Minister’s vision, Government of Maharashtra also has prioritized the Skill Development mission and set a vision of equipping 45 million persons with employable skills by the year 2022.</p>

<p class="para">To achieve this Skill Development Mission in the state of Maharashtra,Maharashtra State Skill Development Society has been registered on 15th February 2011 under the Societies Registration Act 1860. The Society is established under the chairmanship of the Chief Secretary and is the Single Nodal Agency for planning, coordination,execution & monitoring of Skill Development Initiative of Government of Maharashtra.The society is functioning under the Skill Development and Entrepreneurship Department, Government of Maharashtra.</p>

<p class="para">All the Skill Development schemes of various departments of Government of Maharashtra will be integrated and implemented through the active coordination under the single umbrella of this society.</p>

<h4>The key functions of the Society are as follows:</h4>
<p class="para">Prepare Maharashtra State Skill Gap Assessment Report & Skill Development Action Plan.</p>

<p class="para">Support State Departments & District Skill Development Committees in formulation and implementation of Annual Skill Development Plan.</p>

<p class="para">Empanel & ensure suitable accreditation & gradation of the public & private skill training providers and conduct concurrent assessment of training providers to ensure quality of training.</p>

<p class="para">Support and provide assistance to Directorate of Employment & Self-Employment in establishing a dynamic Labour Market Information System & launching of the stakeholder's web portal.</p>

<p class="para">AImplementation of innovative skill development programs.</p>

<p class="para">Support counselling activities, awareness & guidance programs for the workforce in unorganized sector.
Arrange workshops and brainstorming session for the key stakeholders.</p>

<p class="para">Enter into partnership with national & international agencies working in the field of skill development.</p>

<p class="para">Organize study tours for understanding of the best set of national and international practices and experiments in the field of skill development.</p>
<p class="para">Encourage active participation of the public sector undertakings, private sector and civil society through feasible and productive partnerships.</p>
<p class="para">Appoint a “Maharashtra Skill Mission Ambassador” for the representation of the Skill Development Initiative.</p>

<p class="para">Support all initiatives with the objective of motivating the youth towards vocational stream of education from pre-SSC level onwards.</p>
<p class="para">Take all appropriate initiatives and actions in line with the state skill development mission.</p>

<p>With its contribution of 13% to the Gross Domestic Product & with a share of 9.28% in the national population, Government of Maharashtra has set a vision of equipping 45 million persons with employable skills by the year 2022.</p>
