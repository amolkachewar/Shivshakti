	        <h2 style="text-align:center;color:blue;"><b><u>Pradhan Mantri Gramin Digital Saksharta Abhiyan (PMGDISHA)</u></b></h2>
	        
<h4>1. Objective is to make 6 crore persons digitally literate<br></h4>

<h4>2. Duration: 2 years.<br></h4>

<h4>3. Registered candidates are trained for a minimum of 20 hours</h4>
<h4>4. Target Beneficiaries:</h4>
 <p class="para">

Eligible Household: - A household is defined as a unit comprising of Head of family, spouse, children and parents. All such households where none of the family member is digitally literate will be considered as eligible household under the Scheme.</p>
 <h4>5. Entry criteria</h4>
 <p class="para">

 i) The beneficiary should be Digitally Illiterate<br>

 ii) Only one person per eligible household would be considered for training<br>

iii) Age Group: 14 - 60 years</h4>
 <h4>6.  Priority would be given to</h4>
 <p class="para">

 - Non-smartphone users, Antyodaya households, college drop-outs, Participants of the adult literacy mission<br>

 - Digitally illiterate school students from class 9th to 12th, provided facility of Computer/ICT Training is not available in their schools<br>

 - Preference would be given to SC, ST, BPL, women, differently-abled persons and minorities<br>

 - Specifically targeting Scheduled Caste (SC), Scheduled Tribes (ST), Below Poverty Line (BPL), Women, differently-abled, Minorities Aanganwadi workers, Accredited Social Health Activists (ASHAs), Authorised Ration Dealers as key beneficiaries<br></p>
  
 <h4> 7.Physical Infrastructure</h4>
 <p class="para">

- Minimum carpet area of 200 - 500 sq. ft.<br>

 - One class room to seat  5 - 20 students<br>

- Located within the concerned Panchayat boundaries<br>
</p>
 <h4>8. Technical Infrastructur</h4>
 <p class="para"> <br>

- Minimum of 3-5 Operational Computers (Laptop or PC) with genuine/ licensed software<br>

- All computers should be connected on a local network<br>

 - One High Resolution Web Camera (Minimum: 0.5 Megapixel, Recommended: 1280 x 720 resolution)<br>

 - Reliable Internet Connectivity with the minimum speed of 256 kbps<br>

 - One printer and scanner - 3-5 Tablets/Mobile phones<br>

 - Biometric devices prescribed by PMU<br></p>
 
  <h4>9. Human Resources </h4>
 <p class="para"> 

Human Infrastructure Each training centre shall have a minimum of one teaching faculty,  The faculty should have minimum of NIELITâ€™s BCC or its equivalent (B.Sc  IT / B.tech IT, MCA  / BCA / Diploma in Computer etc<br></p>
<h4>10. Implementation Process </h4>
 <p class="para">Authorized Training Partners / Centres with appropriate infrastructure will be utilised to train and empower citizens on IT literacy in Villages/Gram Panchayat/ Blocks/ Districts of each State/ UT. The training will be held at various training locations / Centres</p>
 <h4>11. Training Process:</h4>
 
  <p class="para">The selected beneficiaries shall be registered on the PMGDISHA Portal by the Training Centres. The trainers will use the login to train the beneficiaries and their learning will be tracked centrally.</p>
  <h4> 12. Assessment Process</h4>
  <p class="para"> 
After the completion of training, the trained candidates shall have to undergo a term-end online examination at a centre approved by certifying agency/ PMGDISHA PMU.
</p>
 
