                      <p> <i class="fa fa-angle-double-right" aria-hidden="true"></i>
 Shri Shiv Shakti Education Society focus on skill development has emerged a result of a combination of factors. The changing demographic profile of the country, with 54% of its population under 25 years of age, the rising aspirations of our youth who seek better jobs and higher incomes, and the growing requirements of industry for an efficient, well trained workforce – have contributed to a focus on skill development. Speed, Scale and Quality are the three driving themes of the SSSES efforts.</p>
                      
<p><i class="fa fa-angle-double-right" aria-hidden="true"></i>
The Shri Shiv Shakti Education Society of Skill Development and Entrepreneurship takes a multidimensional view of the term ‘skill’. ‘We recognise the fact that ‘skill development’ has both intrinsic and instrumental value. Possessing a skill can be an end in itself – giving individuals greater  ‘‘self-confidence’’,  ‘‘ self esteem and dignity ’’, which results from the knowledge that they can stand on their own feet. A skill can also be a means to an end. It can a tool, which helps individuals realise their aspirations by pursing better jobs, leading to stable, sustainable livelihoods.</p>


                        </div>
                        <div role="tabpanel" class="tab-pane" id="Connecting"><p><i class="fa fa-angle-double-right" aria-hidden="true"></i>
Skilling is a lifelong process. Hence, skill upgradation and reskilling are fundamental components of the skilling cycle. The Shri Shiv Shakti Education Society also sees a close connection between skilling and entrepreneurship. We seek to create synergies between these two areas, so that our youth can aspire to being job seekers and job creators.</p></div>
                        <div role="tabpanel" class="tab-pane" id="Mission">
                       <p><i class="fa fa-angle-double-right" aria-hidden="true"></i> The Shri Shiv Shakti Education Society Mission has been developed to create convergence across sectors and States in terms of skill training activities. Further, to achieve the vision of ‘Skilled India’, the SSSES Mission would not only consolidate and coordinate skilling efforts, but also expedite decision making across sectors to achieve skilling at scale with speed and standards. It will be implemented through a streamlined institutional mechanism driven by Ministry of Skill Development and Entrepreneurship.</p>                 
                        </div>
                        <div role="tabpanel" class="tab-pane" id="Sub-Mission">
                        <p><i class="fa fa-angle-double-right" aria-hidden="true"></i> Seven sub-missions have been proposed initially to act as building blocks for achieving overall objectives of the Mission. They are:<br>
(i) Institutional Training,<br> (ii) Infrastructure, <br> (iii) Convergence,<br> (iv) Trainers,<br> (v) Overseas Employment,<br> (vi) Sustainable Livelihoods,<br> (vii) Leveraging Public Infrastructure.</p>
                        </div>
