                        <p><i class="fa fa-angle-double-right" aria-hidden="true"></i> The Shri Shiv Shakti education Society has initiated tje Innovations for Skills Marketplace to host information on innovative solutions, practices, models and other relevant research in the skill development space. These can be used by practitioners, Government agencies, policy makers, corporates and other stakeholders that contribute to this growing and vibrant ecosystem.</p>
